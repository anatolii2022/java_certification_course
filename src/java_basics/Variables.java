package java_basics;

public class Variables {

	public static void main(String[] args) {
		 
		int x; // declaring the variable
		x=4; // assigning the value
		
		System.out.println(x);
		
		x=7; // reassigning the variable
		
		System.out.println(x + 4);
		
		System.out.println(x);
		
		String text;
		text = "Hello there";
		
		System.out.println(text + "This is some more text");
 

	}

}
